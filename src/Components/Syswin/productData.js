const oneMgKeys = Object.keys(oneMgProductLinks);

export function getOneMgLinks(productName) {
  const normalized = productName
    .replace(/[-_]/g, ' ')
    .replace(/…/g, '')
    .replace(/\s+/g, ' ')
    .trim()
    .toLowerCase();

  return oneMgKeys
    .filter((key) => key.toLowerCase().startsWith(normalized.split(' ')[0].replace(/[^a-z]/g, '')))
    .filter((key) => {
      const keyBase = key.split(/\s\d/)[0].toLowerCase().trim();
      return keyBase === normalized ||
        normalized.startsWith(keyBase) ||
        keyBase.startsWith(normalized.replace(/[^a-z\s]/g, '').trim());
    })
    .map((key) => ({ label: key, url: oneMgProductLinks[key] }));
}
